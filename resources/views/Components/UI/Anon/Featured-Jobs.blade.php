    document.addEventListener('DOMContentLoaded', function() {
        // Jobs section animations
        gsap.from('.section-header > *', {
            scrollTrigger: {
                trigger: '#jobs-section',
                start: 'top 80%'
            },
            y: 30,
            opacity: 0,
            duration: 0.8,
            stagger: 0.2,
            ease: "power2.out"
        });
        
        gsap.from('.job-card', {
            scrollTrigger: {
                trigger: '.job-cards',
                start: 'top 80%'
            },
            y: 50,
            opacity: 0,
            duration: 0.8,
            stagger: 0.1,
            ease: "power2.out"
        });
        
        gsap.from('.look-more-btn', {
            scrollTrigger: {
                trigger: '.look-more-btn',
                start: 'top 90%'
            },
            y: 30,
            opacity: 0,
            duration: 0.8,
            ease: "back.out(1.7)"
        });
        
        // Job card hover animations
        const jobCards = document.querySelectorAll('.job-card');
        
        jobCards.forEach(card => {
            card.addEventListener('mouseenter', function() {
                gsap.to(this, {
                    y: -10,
                    boxShadow: '0 10px 25px rgba(0, 0, 0, 0.1)',
                    duration: 0.3,
                    ease: "power2.out"
                });
            });
            
            card.addEventListener('mouseleave', function() {
                gsap.to(this, {
                    y: 0,
                    boxShadow: '0 4px 6px -1px rgba(0, 0, 0, 0.1), 0 2px 4px -1px rgba(0, 0, 0, 0.06)',
                    duration: 0.3,
                    ease: "power2.out"
                });
            });
        });
    });
</script>
